  const searchButton = document.getElementById('search-button');
  const departmentInfoContainer = document.getElementById('department-info');
  
  searchButton.addEventListener('click', () => {
    const departmentName = document.getElementById('department-name').value;
    const departmentInfo = collegeData[departmentName];
  
    if (departmentInfo) {
      let infoText = '';
      for (const category in departmentInfo) {
        infoText += `<h2>${category}</h2>`;
        if (Array.isArray(departmentInfo[category])) {
          departmentInfo[category].forEach((item) => {
            infoText += `<p>${item}</p>`;
          });
        } else {
          infoText += `<p>${departmentInfo[category]}</p>`;
        }
      }
      departmentInfoContainer.innerHTML = infoText;
    } else {
      departmentInfoContainer.innerHTML = `<p>No information found for ${departmentName}.</p>`;
    }
  });